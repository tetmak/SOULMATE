    /**
     * Look up coordinates by city/district name.
     * Accepts: "Province - District", "Province", or "Country"
     * @param {string} cityName
     * @returns {{ lat: number, lng: number } | null}
     */
    window.lookupTurkishCity = function(cityName) {
        if (!cityName) return null;
        var name = cityName.trim();
        var parts = name.split(' - ');
        var i, j, c, d;

        // Search Turkish districts first ("Province - District" format)
        if (parts.length === 2) {
            var provName = parts[0].trim();
            var distName = parts[1].trim();
            for (i = 0; i < TURKEY_PROVINCES.length; i++) {
                c = TURKEY_PROVINCES[i];
                if (c.name === provName && c.districts) {
                    for (j = 0; j < c.districts.length; j++) {
                        d = c.districts[j];
                        if (d.name === distName) {
                            return { lat: d.lat, lng: d.lng };
                        }
                    }
                }
            }
        }

        // Search Turkish provinces by name (backward compat)
        for (i = 0; i < TURKEY_PROVINCES.length; i++) {
            c = TURKEY_PROVINCES[i];
            if (c.name === name) {
                // Return Merkez district coords or first district
                if (c.districts && c.districts.length > 0) {
                    for (j = 0; j < c.districts.length; j++) {
                        if (c.districts[j].name === 'Merkez') {
                            return { lat: c.districts[j].lat, lng: c.districts[j].lng };
                        }
                    }
                    return { lat: c.districts[0].lat, lng: c.districts[0].lng };
                }
            }
        }

        // Search world countries
        for (i = 0; i < WORLD_COUNTRIES.length; i++) {
            c = WORLD_COUNTRIES[i];
            if (c.name === name) {
                return { lat: c.lat, lng: c.lng };
            }
        }

        return null;
    };

